GRID_REF_SIZE = 9


def generate_empty_grid(size: int = GRID_REF_SIZE) -> list[list[int]]:
    """Generate a {size x size} 2D-array

    Args:
        size(int): size of the array (square)

    Returns:
        array (list of list): a 2D-array initialized with zeros

    """
    return [[0 for i in range(size)] for j in range(size)]


